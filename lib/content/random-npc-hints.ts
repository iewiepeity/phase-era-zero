// ── 根據玩家狀態選擇最合適的暗示 ─────────────────────────────

export interface HintSelectionContext {
  sceneId:        string;
  visitedCount:   number;   // 已訪問場景數
  clueCount:      number;   // 已收集線索數
  talkedNpcs:     string[]; // 已對話的 NPC ID 列表
  seed:           number;   // 用於確定性選擇
}

/** 根據遊戲狀態選擇暗示（用於注入 System Prompt）*/
export function selectHint(ctx: HintSelectionContext): string {
  const { sceneId, visitedCount, clueCount, seed } = ctx;

  // 決定進度層級
  const progress: "early" | "mid" | "late" =
    visitedCount < 3 || clueCount < 3 ? "early" :
    visitedCount < 6 || clueCount < 7 ? "mid"   :
    "late";

  // 優先使用場景暗示
  const sceneHints = SCENE_HINTS[sceneId];
  if (sceneHints && sceneHints.length > 0) {
    return sceneHints[seed % sceneHints.length];
  }

  // fallback 到通用進度暗示
  const progressHints = PROGRESS_HINTS[progress];
  return progressHints[seed % progressHints.length];
}
