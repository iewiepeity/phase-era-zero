export type RelationshipType =
  | "acquaintance"    // 點頭之交，見過面
  | "colleague"       // 工作關係
  | "informant"       // 情報提供者
  | "observer"        // 默默觀察著對方
  | "suspect_of"      // 懷疑對方是關鍵人物
  | "connected_by"    // 因某事件有所牽連
  | "knows_secret";   // 知道對方的秘密

export interface NpcRelationship {
  npcA:        string;   // npcId
  npcB:        string;   // npcId
  type:        RelationshipType;
  /** A 對 B 的認識（從 A 的視角）*/
  aKnowsB:     string;
  /** B 對 A 的認識（從 B 的視角）*/
  bKnowsA:     string;
  /** 是否雙向（預設 true）*/
  bidirectional?: boolean;
}

