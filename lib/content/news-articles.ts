/**
 * 根據當前幕次取得應顯示的新聞（所有 triggerAct <= currentAct 的文章）。
 * 依發布時間倒序排列（最新在前）。
 */
export function getNewsForAct(currentAct: number): NewsArticle[] {
  return NEWS_ARTICLES
    .filter((a) => a.triggerAct <= currentAct)
    .sort((a, b) => b.triggerAct - a.triggerAct || b.id.localeCompare(a.id));
}
